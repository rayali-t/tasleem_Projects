    private void populateBookingDetails() {
        DefaultTableModel dtm = (DefaultTableModel) tblBookingHistory.getModel();
        dtm.setRowCount(0);

        for (Booking booking : bookingDirectory.getBookingDirectory()) {
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = booking;
            row[1] = booking.getBookingDate();
            row[2] = booking.getFlight().getFlightID();
            row[3] = booking.getFlight().getSource();
            row[4] = booking.getFlight().getDestination();
            row[5] = booking.getFlight().getDate();
            row[6] = booking.getFlight().getFlightcharges();

            dtm.addRow(row);
        }

    }

