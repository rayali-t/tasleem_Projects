    private void btnViewDetails1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetails1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblBookingHistory.getSelectedRow();

        if (selectedRow >= 0) {
            Booking booking = (Booking) tblBookingHistory.getValueAt(selectedRow, 0);
            txtBookingDate.setText(customer.getUsername());

            Flight flight = booking.getFlight();
            txtAirliner.setText(flight.getAirliner().getAirLinerName());
            txtFlightNo.setText(flight.getFlightID());
            txtSource.setText(flight.getSource());
            txtDestination.setText(flight.getDestination());
            txtArrTime.setText(flight.getArrival());
            txtDeptTime.setText(flight.getDeparture());
            txtPrice.setText(String.valueOf(flight.getFlightcharges()));
            txtHalts.setText(String.valueOf(flight.getHalts()));
            txtDate.setText(flight.getDate());
            txtSeat.setText(flight.getAllocatedSeat());

            Customer customer = booking.getCustomer();
            txtUsername.setText(customer.getUsername());
            txtEmailID.setText(customer.getEmailId());
            txtPhoneNo.setText(customer.getPhoneNo());

            txtTravellerName.setText(customer.getTravellerName());

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row for viewing the details.");
        }
    }//GEN-LAST:event_btnViewDetails1ActionPerformed

