    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow =tblDirectory.getSelectedRow();

        if(selectedRow>=0)
        {
            Airliner airliner=(Airliner) tblDirectory.getValueAt(selectedRow, 0);
            ViewAirliner viewPanel = new ViewAirliner(this.panelRight,airliner,this.airlinerDir);
            this.panelRight.add("ViewPanel",viewPanel);
            CardLayout layout = (CardLayout) this.panelRight.getLayout();
            layout.next(this.panelRight);
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }

    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDirectory.getSelectedRow();
        if (selectedRow >= 0) {
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??", "Warning", selectionButton);
            if (selectionResult == JOptionPane.YES_OPTION) {
                Airliner airliner = (Airliner) tblDirectory.getValueAt(selectedRow, 0);
                airlinerDir.deleteAirliner(airliner);
                populate(airlinerDir.getAirlinerDirectory());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

