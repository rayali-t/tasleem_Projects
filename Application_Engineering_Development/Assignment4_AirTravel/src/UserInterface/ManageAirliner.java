    public void populate(ArrayList<Airliner> airDir )
    {
        DefaultTableModel dtm = (DefaultTableModel) tblDirectory.getModel();
        dtm.setRowCount(0);
        
        for(Airliner a: airDir)
        {
            Object[] row = new Object [dtm.getColumnCount()];
            row[0]=a;
            row[1]=a.getNoOfFlights();
            dtm.addRow(row);
        }
    }

