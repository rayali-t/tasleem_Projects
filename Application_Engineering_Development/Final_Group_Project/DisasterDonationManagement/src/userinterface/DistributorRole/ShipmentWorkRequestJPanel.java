    private void btnCreatePackageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreatePackageActionPerformed
        String validationMsg = "";

        if (txtCount.getText() == null || txtCount.getText().isEmpty() || txtCount.getText().trim().isEmpty()) {
            validationMsg += "Shipped count cannot be empty.\n";
        } else {
            try {
                Integer.parseInt(txtCount.getText());
                if (Integer.parseInt(txtCount.getText()) <= 0) {
                    validationMsg += "Shipped count should be greater than 0.\n";
                    txtCount.setText("");
                }
            } catch (NumberFormatException e) {
                validationMsg += "Please enter a valid number in Shipped Count field.\n";
                txtCount.setText("");
            }
        }

        if (!validationMsg.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please review the below error(s):\n" + validationMsg);
            return;
        }

        int itemCount = Integer.parseInt(txtCount.getText());
        int collectedCount = Integer.parseInt(txtCollectedCount.getText());
        if (itemCount > collectedCount) {
            JOptionPane.showMessageDialog(null, "Shipped count should be less or equal to collected count.");
            return;
        }

        //Creating a new request for the transport (to collect and ship)
        String itemName = txtItemName.getText();
        String area = txtArea.getText();

        ShipmentWorkRequest shipmentRequest = new ShipmentWorkRequest();
        shipmentRequest.setItemName(itemName);
        shipmentRequest.setItemCount(itemCount);
        shipmentRequest.setArea(area);
        shipmentRequest.setRequestDate(new Date());
        shipmentRequest.setSender(userAccount);
        shipmentRequest.setStatus("Shipment Ready");

        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof TransportOrganization) {
                org = organization;
                break;
            }
        }

        if (org != null) {
            org.getWorkQueue().getWorkRequestList().add(shipmentRequest);
            userAccount.getWorkQueue().getWorkRequestList().add(shipmentRequest);
        }

        JOptionPane.showMessageDialog(null, "Ready for shipment.");

        txtArea.setText("");
        txtItemName.setText("");
        txtCollectedCount.setText("");
        txtCount.setText("");
        txtCount.setEditable(false);
        txtCount.setEnabled(false);

        for (Requirement req : enterprise.getRequirementDirectory().getRequirementDirectory()) {
            if (req.getItemName().equalsIgnoreCase(itemName)) {
                req.setCollectedCount(collectedCount - itemCount); //Decreasing the count from the collected ones.
            }
        }
        populateRequirementTable();
    }//GEN-LAST:event_btnCreatePackageActionPerformed

    private void btnCreatePackage1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreatePackage1ActionPerformed

        int selectedRow = tblRequirements.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
        txtCount.setEditable(true);
        txtCount.setEnabled(true);

        String area = tblRequirements.getValueAt(selectedRow, 0).toString();
        txtArea.setText(area);

        String itemName = tblRequirements.getValueAt(selectedRow, 1).toString();
        txtItemName.setText(itemName);

        String collectedCount = tblRequirements.getValueAt(selectedRow, 3).toString();
        txtCollectedCount.setText(collectedCount);


    }//GEN-LAST:event_btnCreatePackage1ActionPerformed

