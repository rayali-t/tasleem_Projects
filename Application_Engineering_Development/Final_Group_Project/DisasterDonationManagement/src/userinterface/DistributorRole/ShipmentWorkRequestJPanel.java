    public void populateRequirementTable() {

        //Populate the requirement list table
        DefaultTableModel model = (DefaultTableModel) tblRequirements.getModel();

        model.setRowCount(0);
        for (Requirement item : enterprise.getRequirementDirectory().getRequirementDirectory()) {
            if (item.getRequirementCount() != 0) {
                Object[] row = new Object[4];
                row[0] = item.getArea();
                row[1] = item;
                row[2] = item.getInitialRequirementCount();
                row[3] = item.getCollectedCount();
                model.addRow(row);
            }
        }
    }

