    private void rejectJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rejectJButton1ActionPerformed
        if (txtComments.getText() == null || txtComments.getText().isEmpty() || txtComments.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter reason for rejection.");
            return;
        }
        request.setClaimAcknowledgement(txtComments.getText());
        request.setStatus("Rejected");
        request.setComments(txtComments.getText());
        txtComments.setEnabled(false);
        JOptionPane.showMessageDialog(null, "Request rejected successfully");
        submitJButton.setEnabled(false);
        rejectJButton1.setEnabled(false);
    }//GEN-LAST:event_rejectJButton1ActionPerformed


