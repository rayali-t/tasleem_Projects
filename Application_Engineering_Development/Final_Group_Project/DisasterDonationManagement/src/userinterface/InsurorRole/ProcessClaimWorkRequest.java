    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed

        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        InsurorRoleWorkAreaJPanel dwjp = (InsurorRoleWorkAreaJPanel) component;
        dwjp.populateInsurorTable();
        //dwjp.populateRequirementsTable();
        dwjp.populateHistoricInsurorRequests();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backJButtonActionPerformed

    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        if (txtComments.getText() == null || txtComments.getText().isEmpty() || txtComments.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter comments before accepting.");
            return;
        }
        request.setClaimAcknowledgement(txtComments.getText());
        request.setStatus("Completed");
        request.setComments(txtComments.getText());
        txtComments.setEnabled(false);
        JOptionPane.showMessageDialog(null, "Request accepted successfully");
        submitJButton.setEnabled(false);
        rejectJButton1.setEnabled(false);
    }//GEN-LAST:event_submitJButtonActionPerformed

